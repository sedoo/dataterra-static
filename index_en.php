                    <h3>Aeris pole</h3>
                    <p>Atmospheric Research concern domains such as dynamics, atmospheric physics and chemistry and also include more climate change-oriented studies. To carry out these research activities, the scientific community use models as well as observations that are obtained by means of satellite, airborne vehicle and ground-based facilities<br>
                    One of the challenges of this century is to make available all these observations to a broad community, for research and commercial applications. To be useful, these data must be of high quality and well documented (be calibrated, validated, and homogenized)</p>

                    <h2>Land<br>surfaces</h2>
                    <h3>Theia pole</h3>
                    <p>Theia Data and Services centre for land surfaces was created at the end of 2012 by 9 French public institutions involved in Earth observation and environmental sciences. The purpose of this scientific and technical structure is to facilitate the use of images resulting from the spatial observation of continental surfaces.<br>
                    Theia provides the national and international scientific community and public policies aiming to monitor and manage environmental resources with a wide range of images at different scales, products, methods and training related to the observation of continental surfaces, particularly from space.</p>
                    <a href="https://www.theia-land.fr/en/data-and-services-for-the-land/" target="_blank" class="btn">Visit THEIA website</a>

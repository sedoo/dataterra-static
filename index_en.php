                        <h2>Oceano<br class="hidden-lg">graphy</h2>
                        <h3>Odatis pole</h3>
                        <p>ODATIS (Ocean Data Information and Services) is the entry point to access all the French Ocean observation data.
Its aim is to promote and facilitate the use of Ocean observations sampled by way of in situ and remote sensing measurements.
ODATIS contributes to describing, quantifying and understanding the ocean as a whole — offshore and coastal — regarding aspects such as the dynamics and thermodynamics of the ocean, the evolution of its physico-chemical properties, the biogeochemical cycles, the functioning of ecosystems, the ocean evolution and the ocean-climate relationship in the past, etc.</p>
